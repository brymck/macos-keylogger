func ConvertKeyCode(keyCode int) (rune, error) {
	if keyCode < 0 || keyCode >= len(keyCodeKeyMapping) {
		return ' ', ErrInvalidKeyCode
	}

	k := keyCodeKeyMapping[keyCode]
	if k == ' ' {
		return ' ', ErrInvalidKeyCode
	}

	return k, nil
}
